const remoteLoadLimits = {
  users: 250,
  notifications: 250,
  auditLogs: 500,
  analyticsEvents: 1000,
  analyticsTargets: 200,
} as const

interface RemoteState {
  state: AppDataState
  lookupValues: LookupValue[]
}

export function createSupabaseShellState(profile: LeadraUser): AppDataState {
  return {
    users: [profile],
    units: [],
    branches: [],
    teams: [],
    notifications: [],
    auditLogs: [],
    analyticsEvents: [],
    analyticsTargets: [],
    settings: {
      companyName: 'Leadra',
      commissionPercentage: 1.5,
      footerText: 'Generated by Leadra resale system',
      contactDetails: '',
      logoPath: '',
      pdfLayout: 'classic',
      mediaLimitMb: 40,
      paymentMethods: ['cash', 'installment'],
    },
  }
}

interface SafeAuditLogRow {
  id: string
  actor_name: string | null
  actor_role: UserRole
  action_type: string
  message_key: string | null
  message_params: MessageParams | null
  related_unit_code: string | null
  previous_value: unknown
  new_value: unknown
  ip_address: string | null
  created_at: string
}

export async function loadSupabaseProfile(client: SupabaseClient, authUser: User): Promise<LeadraUser> {
  const { data, error } = await client.from('profiles').select('*').eq('id', authUser.id).single()
  if (error) throw new Error(`Profile load failed: ${error.message}`)
  if (!data) throw new Error('No profile exists for this Supabase user.')

  return toLeadraUser(data)
}

export async function markSupabaseLogin(client: SupabaseClient): Promise<void> {
  const { error } = await client.rpc('mark_own_login')
  if (error) console.warn('Supabase login timestamp was not updated:', error.message)
}

export async function setSupabaseThemePreference(client: SupabaseClient, themePreference: ThemePreference): Promise<LeadraUser> {
  const { data, error } = await client.rpc('set_own_theme_preference', { theme: themePreference })
  if (error) throw new Error(`Theme update failed: ${error.message}`)
  const profile = Array.isArray(data) ? data[0] : data
  if (!profile) throw new Error('Theme update failed.')
  return toLeadraUser(profile as Record<string, unknown>)
}

export async function loadSupabaseAppState(client: SupabaseClient): Promise<RemoteState> {
  const repository = new LeadraRepository(client)
  await repository.reconcileDueUnitPayments()
  const [users, units, branches, teams, lookupValues, settings, notifications, auditLogs, analyticsEvents, analyticsTargets] = await Promise.all([
    loadProfiles(client),
    repository.listUnits(),
    loadBranches(client),
    loadTeams(client),
    loadLookupValues(client),
    loadSettings(client),
    loadNotifications(client),
    loadAuditLogs(client),
    loadAnalyticsEvents(client),
    loadAnalyticsTargets(client),
  ])

  return {
    lookupValues,
    state: {
      users,
      units,
      branches,
      teams,
      notifications,
      auditLogs,
      analyticsEvents,
      analyticsTargets,
      settings,
    },
  }
}

export async function loadSupabaseAnalyticsDashboard(client: SupabaseClient, filters: AnalyticsFilters): Promise<AnalyticsDashboard> {
  const { data, error } = await client.rpc('analytics_dashboard', { filters })
  if (error) throw new Error(`Analytics load failed: ${error.message}`)
  return toAnalyticsDashboard(data as Record<string, unknown>)
}

async function loadProfiles(client: SupabaseClient): Promise<LeadraUser[]> {
  const { data, error } = await client.from('profiles').select('*').order('role').order('full_name').limit(remoteLoadLimits.users)
  if (error) throw new Error(`Users load failed: ${error.message}`)
  return (data ?? []).map(toLeadraUser)
}

async function loadLookupValues(client: SupabaseClient): Promise<LookupValue[]> {
  const { data, error } = await client
    .from('lookup_values')
    .select('id, kind, label, thumbnail_path, archived')
    .eq('archived', false)
    .order('kind')
  if (error) throw new Error(`Lookup values load failed: ${error.message}`)
  return (data ?? [])
    .map((row) => ({
      id: String(row.id),
      kind: row.kind,
      label: row.label,
      thumbnailPath: row.thumbnail_path,
      archived: row.archived,
    }))
    .sort((first, second) => compareText('en', first.label, second.label))
}

async function loadSettings(client: SupabaseClient): Promise<AppSettings> {
  const { data, error } = await client.from('app_settings').select('*').eq('id', true).single()
  if (error) throw new Error(`Settings load failed: ${error.message}`)
  return {
    companyName: data.company_name ?? 'Leadra',
    commissionPercentage: Number(data.commission_percentage ?? 1.5),
    footerText: data.footer_text ?? 'Generated by Leadra resale system',
    contactDetails: data.contact_details ?? '',
    logoPath: data.logo_path ?? '',
    pdfLayout: data.pdf_layout === 'compact' ? 'compact' : 'classic',
    mediaLimitMb: Number(data.media_limit_mb ?? 40),
    paymentMethods: ['cash', 'installment'],
  }
}

async function loadNotifications(client: SupabaseClient): Promise<NotificationItem[]> {
  const { data, error } = await client
    .from('notifications')
    .select('id, user_id, audience_role, title, body, message_key, message_params, read_at, created_at')
    .order('created_at', { ascending: false })
    .limit(remoteLoadLimits.notifications)
  if (error) throw new Error(`Notifications load failed: ${error.message}`)
  return ((data ?? []) as Record<string, unknown>[]).map((row) => ({
    id: String(row.id),
    title: String(row.title ?? ''),
    body: String(row.body ?? ''),
    messageKey: typeof row.message_key === 'string' ? row.message_key : null,
    messageParams: row.message_params && typeof row.message_params === 'object' ? row.message_params : null,
    audienceRole: typeof row.audience_role === 'string' ? row.audience_role : undefined,
    userId: typeof row.user_id === 'string' ? row.user_id : undefined,
    createdAt: String(row.created_at ?? ''),
    read: Boolean(row.read_at),
  }) as NotificationItem)
}

async function loadAuditLogs(client: SupabaseClient): Promise<AuditLogItem[]> {
  const { data, error } = await client.rpc('list_audit_logs_safe', { limit_count: remoteLoadLimits.auditLogs })
  if (error) throw new Error(`Audit logs load failed: ${error.message}`)
  return ((data ?? []) as SafeAuditLogRow[]).map((row) => ({
    id: String(row.id),
    actorName: row.actor_name ?? 'Leadra user',
    actorRole: row.actor_role,
    actionType: row.action_type,
    messageKey: row.message_key ?? null,
    messageParams: row.message_params ?? null,
    relatedUnitCode: row.related_unit_code ?? undefined,
    previousValue: row.previous_value == null ? null : JSON.stringify(row.previous_value),
    newValue: row.new_value == null ? null : JSON.stringify(row.new_value),
    ipAddress: row.ip_address,
    createdAt: row.created_at,
  }))
}

