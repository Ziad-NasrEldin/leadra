function toLeadraUser(row: Record<string, unknown>): LeadraUser {
  return {
    id: String(row.id),
    fullName: String(row.full_name ?? 'Leadra user'),
    email: String(row.email ?? ''),
    role: String(row.role ?? 'sales') as UserRole,
    jobTitle: String(row.job_title ?? 'Team member'),
    phoneNumber: String(row.phone_number ?? ''),
    teamId: String(row.team_id ?? ''),
    branchId: String(row.branch_id ?? ''),
    status: row.status === 'inactive' ? 'inactive' : 'active',
    themePreference: row.theme_preference === 'dark' ? 'dark' : 'light',
    createdAt: typeof row.created_at === 'string' ? row.created_at : undefined,
    lastLoginAt: typeof row.last_login_at === 'string' ? row.last_login_at : null,
    deletedAt: typeof row.deleted_at === 'string' ? row.deleted_at : null,
  }
}

