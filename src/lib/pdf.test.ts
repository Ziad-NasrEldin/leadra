import { describe, expect, it } from 'vitest'
import { demoUsers, initialAppState, seedUnits } from '../data/seed'
import { buildPermissionSafePdfBlob, buildPermissionSafePdfText } from './pdf'

describe('pdf generation', () => {
  it('builds a privacy-safe printable brief blob with branding and generator name only', async () => {
    const user = demoUsers[3]
    const unit = { ...seedUnits[0], media: [] }
    const settings = initialAppState.settings

    const text = buildPermissionSafePdfText(user, unit, settings)
    const blob = await buildPermissionSafePdfBlob(user, unit, settings)
    const header = await blob.slice(0, 5).text()

    expect(blob.type).toBe('application/pdf')
    expect(header).toBe('%PDF-')
    expect(text).toContain(settings.companyName)
    expect(text).toContain(`Generated by: ${user.fullName}`)
    expect(text).toContain(settings.footerText)
    expect(text).toContain(settings.contactDetails)
    expect(text).not.toContain(user.phoneNumber)
    expect(text).not.toContain(user.role.replace('_', ' '))
    expect(text).not.toContain(unit.originalOwnerName)
    expect(text).not.toContain(unit.originalOwnerPhone)
  })

  it('hides sales-only export fields when sales exports another user unit', async () => {
    const user = demoUsers[3]
    const unit = {
      ...seedUnits[0],
      createdBy: 'another-sales-user',
      salesNotes: 'Internal sales-only negotiation note',
      commissionAmount: 150000,
      commissionPercentage: 2,
    }
    const settings = initialAppState.settings

    const text = buildPermissionSafePdfText(user, unit, settings)
    const blob = await buildPermissionSafePdfBlob(user, { ...unit, media: [] }, settings)
    const header = await blob.slice(0, 5).text()

    expect(text).not.toContain('Internal sales-only negotiation note')
    expect(text).not.toContain('Commission')
    expect(text).not.toContain('150,000')
    expect(header).toBe('%PDF-')
  })

  it('builds a valid branded pdf with images while tolerating corrupt image data', async () => {
    const user = demoUsers[0]
    const settings = initialAppState.settings
    const unit = {
      ...seedUnits[0],
      media: [
        {
          id: 'media-test-png',
          type: 'image' as const,
          name: 'tiny.png',
          sizeBytes: 68,
          url: 'data:image/png;base64,iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAYAAAAfFcSJAAAADUlEQVR42mP8z8BQDwAFgwJ/lZrD9wAAAABJRU5ErkJggg==',
        },
        {
          id: 'media-bad',
          type: 'image' as const,
          name: 'bad.png',
          sizeBytes: 4,
          url: 'data:image/png;base64,bad',
        },
      ],
    }

    const blob = await buildPermissionSafePdfBlob(user, unit, settings, 'ar')
    const header = await blob.slice(0, 5).text()

    expect(blob.type).toBe('application/pdf')
    expect(header).toBe('%PDF-')
    expect(blob.size).toBeGreaterThan(1000)
  })
})
