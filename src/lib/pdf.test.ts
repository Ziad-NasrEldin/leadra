import { describe, expect, it } from 'vitest'
import { demoUsers, seedUnits } from '../data/seed'
import { buildPermissionSafePdfBlob, buildPermissionSafePdfText } from './pdf'

describe('pdf generation', () => {
  it('builds a valid pdf blob with generator contact details', async () => {
    const user = demoUsers[3]
    const unit = seedUnits[0]

    const text = buildPermissionSafePdfText(user, unit)
    const blob = buildPermissionSafePdfBlob(user, unit)
    const pdf = await blob.text()

    expect(blob.type).toBe('application/pdf')
    expect(pdf.startsWith('%PDF-1.4')).toBe(true)
    expect(pdf).toContain('%%EOF')
    expect(text).toContain(`Generated by: ${user.fullName}`)
    expect(text).toContain(`Generator phone: ${user.phoneNumber}`)
    expect(pdf).toContain(`Generated by: ${user.fullName}`)
    expect(pdf).toContain(`Generator phone: ${user.phoneNumber}`)
  })
})
